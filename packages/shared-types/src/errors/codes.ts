  // ===== SYSTEM =====
  SYSTEM_UNKNOWN = 'SYSTEM_UNKNOWN',
  SYSTEM_INTERNAL_ERROR = 'SYSTEM_INTERNAL_ERROR',
  SYSTEM_MAINTENANCE = 'SYSTEM_MAINTENANCE',
  SYSTEM_CONFIG_INVALID = 'SYSTEM_CONFIG_INVALID',
  SYSTEM_DB_CONNECTION_FAILED = 'SYSTEM_DB_CONNECTION_FAILED',
  SYSTEM_REDIS_CONNECTION_FAILED = 'SYSTEM_REDIS_CONNECTION_FAILED',
  SYSTEM_DEPENDENCY_TIMEOUT = 'SYSTEM_DEPENDENCY_TIMEOUT',

  // ===== AUTH（401 族） =====
  AUTH_UNAUTHENTICATED = 'AUTH_UNAUTHENTICATED',
  AUTH_INVALID_CREDENTIALS = 'AUTH_INVALID_CREDENTIALS',
  AUTH_TOKEN_EXPIRED = 'AUTH_TOKEN_EXPIRED',
  AUTH_TOKEN_INVALID = 'AUTH_TOKEN_INVALID',
  AUTH_TOKEN_REVOKED = 'AUTH_TOKEN_REVOKED',
  AUTH_OTP_EXPIRED = 'AUTH_OTP_EXPIRED',
  AUTH_OTP_INVALID = 'AUTH_OTP_INVALID',
  AUTH_OTP_RATE_LIMITED = 'AUTH_OTP_RATE_LIMITED',
  AUTH_ACCOUNT_LOCKED = 'AUTH_ACCOUNT_LOCKED',
  AUTH_ACCOUNT_DISABLED = 'AUTH_ACCOUNT_DISABLED',
  AUTH_MAGIC_LINK_EXPIRED = 'AUTH_MAGIC_LINK_EXPIRED',
  AUTH_MAGIC_LINK_INVALID = 'AUTH_MAGIC_LINK_INVALID',
  AUTH_SESSION_INVALID = 'AUTH_SESSION_INVALID',

  // ===== PERMISSION（403 族） =====
  PERMISSION_DENIED = 'PERMISSION_DENIED',
  PERMISSION_SCOPE_INSUFFICIENT = 'PERMISSION_SCOPE_INSUFFICIENT',

  // ===== TENANT =====
  TENANT_NOT_FOUND = 'TENANT_NOT_FOUND',
  TENANT_SUSPENDED = 'TENANT_SUSPENDED',
  TENANT_INACTIVE = 'TENANT_INACTIVE',
  TENANT_QUOTA_EXCEEDED = 'TENANT_QUOTA_EXCEEDED',
  TENANT_ISOLATION_VIOLATION = 'TENANT_ISOLATION_VIOLATION',

