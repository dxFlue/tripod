  VALIDATION_FAILED = 'VALIDATION_FAILED',
  VALIDATION_MISSING_FIELD = 'VALIDATION_MISSING_FIELD',
  VALIDATION_INVALID_FORMAT = 'VALIDATION_INVALID_FORMAT',

  // ===== RATE LIMIT =====
  RATE_LIMIT_EXCEEDED = 'RATE_LIMIT_EXCEEDED',

