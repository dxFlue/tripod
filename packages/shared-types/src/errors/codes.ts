/**
 * M2 错误码枚举。
 *
 * 命名规范：`<DOMAIN>_<SPECIFIC_REASON>`
 * - DOMAIN：SYSTEM / AUTH / PERMISSION / TENANT / VALIDATION / RATE_LIMIT / NOT_FOUND / CONFLICT / QUOTA / QUEUE
 * - 业务 domain（ORDER / PAYMENT / SHIPPING 等）由业务 shared-* 或 apps/server 各自扩展
 *
 * 4 语言翻译契约：每个 code 必须在 i18n-file/messages/{zh-CN,en-US,zh-TW,ja-JP}.json 有对应翻译键
 * （shared-i18n 启动时校验，缺译 fail-fast）。
 */
